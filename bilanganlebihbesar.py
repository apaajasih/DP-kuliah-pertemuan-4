#Tuliskan program yang meminta input dua bilangan dan menampilkan bilangan yang lebih besar.

nilai_1 = int(input("Masukkan angka pertama: " ))
nilai_2 = int(input("Masukkan angka kedua: " ))
if nilai_1 > nilai_2:
    print("nilai", nilai_1, "lebih besar dari ", nilai_2)
else:
    print("nilai", nilai_2, "lebih besar dari ", nilai_1)

